    head_y = head[1];

    if (trav_x < head_x) {
        return 1; //left
    } else if (trav_x > head_x) {
        return 2; //right
    } else if (trav_y < head_y) {
        return 3; //up
    } else if (trav_y > head_y) {
        return 4; //down
    }
